package com.example.dex.router;

/**
 * Политика маршрутизации ордера клиента.
 */
public enum RoutingPolicy {
    BEST_EXECUTION, // Автоматический выбор лучшей цены и распределение объемов
    FORCE_SMR,      // Принудительная отправка только в децентрализованную SMR-сеть
    FORCE_L2        // Принудительная отправка только в быстрый L2-секвенсор
}
